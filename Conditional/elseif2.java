package Conditional;

public class elseif2 {

	public static void main(String[] args) {

		
		int num=10;
		if(num/2==0)
		{
			System.out.println("divisible by two");
		}
		else if(num/4==0)
		{
			System.out.println("divisible by four");
		}
		else
		{
			System.out.println("not divisible by anyone");
		}
	}

}
